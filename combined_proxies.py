def sort_proxies_by_country_and_city(proxies_dict):
    """
    Sort proxies by country first, then by city.
    Cities with None values will appear first within each country.
    
    Args:
        proxies_dict (dict): Dictionary of proxies with IP as key
        
    Returns:
        dict: Sorted dictionary maintaining order
    """
    # Convert to list of tuples for sorting
    proxy_items = list(proxies_dict.items())
    
    # Sort by country first, then by city (None values first)
    sorted_items = sorted(proxy_items, key=lambda x: (
        x[1]["country"], 
        x[1]["city"] if x[1]["city"] is not None else ""
    ))
    
    # Convert back to dictionary (maintains order in Python 3.7+)
    return dict(sorted_items)

def get_proxies_by_country(proxies_dict, country):
    """
    Get all proxies from a specific country.
    
    Args:
        proxies_dict (dict): Dictionary of proxies
        country (str): Country name to filter by
        
    Returns:
        dict: Filtered proxies from the specified country
    """
    return {ip: data for ip, data in proxies_dict.items() 
            if data["country"] == country}

def get_countries_list(proxies_dict):
    """
    Get a list of all unique countries in the proxies.
    
    Args:
        proxies_dict (dict): Dictionary of proxies
        
    Returns:
        list: Sorted list of unique countries
    """
    countries = set(data["country"] for data in proxies_dict.values())
    return sorted(countries)

